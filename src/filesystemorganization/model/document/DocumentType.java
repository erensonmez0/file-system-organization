package filesystemorganization.model.document;

/**
 * This enum represents different types of documents.
 *
 * @author ujxbs
 */
public enum DocumentType {

    /**
     * Image document.
     */
    IMAGE,

    /**
     * Audio document.
     */
    AUDIO,

    /**
     * Video document.
     */
    VIDEO,

    /**
     * Text document.
     */
    TEXT,

    /**
     * Program document.
     */
    PROGRAM
}
